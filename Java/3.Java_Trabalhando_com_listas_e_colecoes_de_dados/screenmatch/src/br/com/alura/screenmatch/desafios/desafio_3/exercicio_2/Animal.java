package br.com.alura.screenmatch.desafios.desafio_3.exercicio_2;

public class Animal {

    private String especie;
    private String nome;

    public Animal(String especie, String nome) {
        this.especie = especie;
        this.nome = nome;
    }

    public String getEspecie() {
        return this.especie;
    }

    public String getNome() {
        return this.nome;
    }

}
